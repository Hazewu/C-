#include<iostream>
using namespace std;

int main(){
    int i;
    char c;
    float f;
    cin>>i>>c>>f;                         //consecutive input
    cout<<"i="<<i<<" c="<<c<<" f="<<f;    //consecutive output
    return 0;    
}
